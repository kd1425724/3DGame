#pragma once

//====================================================
//
// IStaticCollider ── 静的な世界コリジョン(建物/地面/ブロック等)がCollisionGridに載るための印
//
//  ・これを実装したKdGameObject派生は、CollisionGridが空間登録し、近傍クエリで返す。
//    キャラの接地/壁判定は「近くの物」だけ調べれば足りるので、全オブジェクト総当たりを避けられる。
//  ・動く物(プレイヤー/敵)は実装しない=グリッドに載らず、従来どおり個別に扱う。
//  ・将来ここに「動的コリジョン用の別インタフェース」を足す等、拡張の起点にする想定。
//
//====================================================
class IStaticCollider
{
public:

	virtual ~IStaticCollider() {}

	// 当たり判定の大まかな範囲(ワールド境界球)。グリッドのどのセルに登録するかの判断に使う。
	// ※ ここは"ふるい"用の粗い範囲でよい。実際の当たりは各オブジェクトのIntersectsが精密に行う。
	virtual DirectX::BoundingSphere GetColliderBounds() const = 0;
};
